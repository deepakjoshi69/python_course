# program to count number of 0 in a tuple

a = (7, 0, 8, 0, 0, 9)
count_zeros = a.count(0)
print("Number of 0s in the tuple:", count_zeros)
print("Minimum value in the tuple:", min(a))
print("Maximum value in tuple:", max(a))
print("Maximum value in tuple:", sum(a))
print("Maximum value in tuple:", sorted(a))